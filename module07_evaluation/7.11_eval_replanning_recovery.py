import json

from utils.ollama_client import chat


def llm_answer(system_prompt: str, user_prompt: str) -> str:
    try:
        return chat([
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ]).strip()
    except Exception as exc:
        return f"ERROR: {exc}"


def parse_json(text: str) -> dict:
    try:
        data = json.loads(text)
        return data if isinstance(data, dict) else {}
    except Exception:
        return {}


def score_recovery(data: dict) -> dict:
    actions = [str(x).lower() for x in data.get("recovery_actions", [])]
    final_status = str(data.get("final_status", "")).lower()
    has_fallback = any("fallback" in a or "retry" in a for a in actions)
    has_degraded = any("degraded" in a or "partial" in a for a in actions)
    success = int(final_status in {"success", "partial_success"})
    score = round((0.4 * int(has_fallback)) + (0.3 * int(has_degraded)) + (0.3 * success), 3)
    return {
        "has_fallback_or_retry": int(has_fallback),
        "has_degraded_mode": int(has_degraded),
        "final_success": success,
        "score": score,
    }


if __name__ == "__main__":
    incident = (
        "Scenario: SQL generated by the agent is rejected by safe_sql guard because it is not a SELECT. "
        "Return strict JSON: {\"recovery_actions\": [str], \"final_status\": str}."
    )

    baseline = llm_answer("Respond with a recovery approach.", incident)
    improved = llm_answer(
        "Use replanning with safe SQL rewrite to SELECT, retry execution, and transparent fallback summary if still blocked.",
        incident,
    )

    baseline_score = score_recovery(parse_json(baseline))
    improved_score = score_recovery(parse_json(improved))

    print({
        "baseline": {"score": baseline_score.get("score", 0), "details": baseline_score},
        "improved": {"score": improved_score.get("score", 0), "details": improved_score},
    })
